"""Typed artifact manifest models for evaluation runs."""

from __future__ import annotations

from datetime import datetime
from enum import StrEnum

from pydantic import Field

from backend.app.orchestration.enums import ArtifactKind, StageName
from resume_optimizer.models import NonEmptyStr, StableId, StrictModel


class ArtifactPayloadFormat(StrEnum):
    """On-disk payload format for a persisted evaluation artifact."""

    JSON = "json"
    TEXT = "text"
    BINARY = "binary"


class PersistedArtifactMetadata(StrictModel):
    """Metadata written alongside each persisted artifact."""

    artifact_id: StableId
    run_id: StableId
    case_id: StableId
    stage_name: StageName
    artifact_kind: ArtifactKind
    schema_version: NonEmptyStr
    persisted_at: datetime
    content_type: NonEmptyStr
    content_hash: NonEmptyStr | None = None
    redacted: bool = False
    redaction_policy: NonEmptyStr = "phase7.basic_redaction.v1"
    payload_format: ArtifactPayloadFormat
    size_bytes: int | None = Field(default=None, ge=0)


class LoadedArtifactDocument(StrictModel):
    """Loaded artifact document reconstructed from disk."""

    entry: "ArtifactManifestEntry"
    metadata: PersistedArtifactMetadata
    payload: dict[str, object] | list[object] | str | None = None


class ArtifactManifestEntry(StrictModel):
    """One persisted stage artifact generated by an evaluation run."""

    artifact_id: StableId
    run_id: StableId
    case_id: StableId
    stage_name: StageName
    artifact_kind: ArtifactKind
    schema_version: NonEmptyStr
    storage_path: NonEmptyStr
    relative_path: NonEmptyStr
    content_type: NonEmptyStr
    payload_format: ArtifactPayloadFormat
    metadata_path: NonEmptyStr | None = None
    content_hash: NonEmptyStr | None = None
    size_bytes: int | None = Field(default=None, ge=0)
    created_at: datetime
    redacted: bool = False


class ArtifactManifest(StrictModel):
    """Artifact inventory for one evaluation case run."""

    run_id: StableId
    case_id: StableId
    schema_version: NonEmptyStr = "phase7.eval.manifest.v1"
    generated_at: datetime
    entries: list[ArtifactManifestEntry] = Field(default_factory=list)
